package com.openddal.server.protocol.mysql.packet;

import io.mycat.net.NetSystem;
import io.mycat.server.GenalMySQLConnection;
import io.mycat.server.packet.util.BufferUtil;

import java.nio.ByteBuffer;

/**
 * From client to server when the client do heartbeat between mycat cluster.
 * <p>
 * <pre>
 * Bytes         Name
 * -----         ----
 * 1             command
 * n             id
 */
public class HeartbeatPacket extends MySQLPacket {

    public byte command;
    public long id;

    public void read(byte[] data) {
        MySQLMessage mm = new MySQLMessage(data);
        packetLength = mm.readUB3();
        packetId = mm.read();
        command = mm.read();
        id = mm.readLength();
    }

    public void write(GenalMySQLConnection c) {
        ByteBuffer buffer = NetSystem.getInstance().getBufferPool().allocate();
        BufferUtil.writeUB3(buffer, calcPacketSize());
        buffer.put(packetId);
        buffer.put(command);
        BufferUtil.writeLength(buffer, id);
        c.write(buffer);
    }

    @Override
    public int calcPacketSize() {
        return 1 + BufferUtil.getLength(id);
    }

    @Override
    protected String getPacketInfo() {
        return "Mycat Heartbeat Packet";
    }

}